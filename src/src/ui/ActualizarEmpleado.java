    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        this.empleado.setNombre(jTextField6.getText());
        this.empleado.setApellido(jTextField7.getText());
        this.empleado.setEmail(jTextField5.getText());

        if (jComboBox1.getSelectedItem().equals("Administrador")) {
            empleado.setCargo_id(1);
        } else if (jComboBox1.getSelectedItem().equals("Gerente")) {
            empleado.setCargo_id(2);
        } else if (jComboBox1.getSelectedItem().equals("Supervisor")) {
            empleado.setCargo_id(3);
        } else if (jComboBox1.getSelectedItem().equals("Contador")) {
            empleado.setCargo_id(4);
        }

        if (jTextField3.getText().equals(jTextField2.getText())) {
            empleado.setPassword(jTextField3.getText());
            try {
                empleado.modificarEmpleado();
                JOptionPane.showMessageDialog(this, "Se Actualizo correctamente");
                jTextField6.setText("");
                jTextField7.setText("");
                jTextField5.setText("");
                jTextField3.setText("");
                jTextField2.setText("");
                jTextField1.setText("");
                jComboBox1.removeAllItems();

            } catch (SQLException ex) {
                Logger.getLogger(ActualizarEmpleado.class.getName()).log(Level.SEVERE, null, ex);
            }
        }


    }//GEN-LAST:event_jButton2ActionPerformed

