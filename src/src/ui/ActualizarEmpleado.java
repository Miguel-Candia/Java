    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String rut = jTextField1.getText();
        this.empleado = new Empleado();
        try {
            empleado.consultarEmpleado(rut);
        } catch (SQLException ex) {
            Logger.getLogger(BuscarEmpleado.class.getName()).log(Level.SEVERE, null, ex);
        }
        Cargo cargo = new Cargo();

        jTextField6.setText(empleado.getNombre());
        jTextField7.setText(empleado.getApellido());
        jTextField5.setText(empleado.getEmail());
        jTextField3.setText(empleado.getPassword());

        List<Cargo> listCargo;
        try {
            listCargo = new Cargo().getCargos();
            jComboBox1.addItem(listCargo.get(0).getNombre());
            jComboBox1.addItem(listCargo.get(1).getNombre());
            jComboBox1.addItem(listCargo.get(2).getNombre());
            jComboBox1.addItem(listCargo.get(3).getNombre());

            if (empleado.getCargo_id() == 1) {
                jComboBox1.setSelectedIndex(0);
            } else if (empleado.getCargo_id() == 2) {
                jComboBox1.setSelectedIndex(1);
            } else if (empleado.getCargo_id() == 3) {
                jComboBox1.setSelectedIndex(2);
            } else if (empleado.getCargo_id() == 4) {
                jComboBox1.setSelectedIndex(3);
            }
        } catch (SQLException ex) {
            Logger.getLogger(ActualizarEmpleado.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

